// Exploring let and const

// 1) const is way to create constant in js which can't be re-assign and re-declare

const a = 12;
// a = 14; // It will produce an array
console.log(a)

// 2) let is used to create a variable in js it can be re-assign

let b = 12;
console.log(b) // output = 12
b = 14;
console.log(b) // output = 14

